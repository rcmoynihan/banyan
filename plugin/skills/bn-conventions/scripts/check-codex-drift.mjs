#!/usr/bin/env node
// check-codex-drift.mjs -- enforce that dist/codex/ is the generator's output of
// the current plugin/ source. plugin/ is the single source for both hosts; this
// gate turns that from a design invariant into a runnable failure.
//
// The check re-renders plugin/ in memory and compares the freshly generated tree
// against the committed dist/codex/. Any difference -- a plugin/ edit that was
// never regenerated, or a hand-edit of dist/codex/ -- is reported as drift with
// the offending paths and the remediation line.
//
// Usage:
//   node check-codex-drift.mjs [--root <repo-root>] [--write-manifest]
//
// --root            repo root holding plugin/ and dist/codex/
//                   (defaults to four levels above this script).
// --write-manifest  write dist/codex/.build-manifest.json from the fresh render
//                   instead of checking; used to regenerate the manifest.
//
// Exit 0 clean, exit 1 on drift. Zero dependencies: node:* only.

import fs from 'node:fs';
import path from 'node:path';
import crypto from 'node:crypto';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

import { render } from '../../../../scripts/codex-build/render-codex.mjs';
import { isEntryPoint } from './entry-point.mjs';

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const DEFAULT_ROOT = path.resolve(SCRIPT_DIR, '..', '..', '..', '..');

const DIST_REL = path.join('dist', 'codex');
const MANIFEST_NAME = '.build-manifest.json';
const REMEDIATION =
  'run `node scripts/codex-build/render-codex.mjs` then ' +
  '`node plugin/skills/bn-conventions/scripts/check-codex-drift.mjs --write-manifest`, ' +
  'and commit.';

function sha256(text) {
  return crypto.createHash('sha256').update(text, 'utf8').digest('hex');
}

// The (relativePath -> { content, source }) map the generator's render() output
// implies. Relative paths are POSIX-joined under dist/codex/ so the manifest is
// stable across platforms. source is the plugin/ file each output derives from.
function expectedOutputs(result) {
  const outputs = new Map();
  for (const agent of result.agents) {
    outputs.set(`agents/${agent.name}.toml`, {
      content: agent.toml,
      source: `plugin/agents/${agent.name}.md`,
    });
  }
  for (const skill of result.skills) {
    outputs.set(`skills/${skill.name}/SKILL.md`, {
      content: skill.skillMd,
      source: `plugin/skills/${skill.name}/SKILL.md`,
    });
  }
  outputs.set('AGENTS.md', {
    content: result.agentsMd,
    source: 'plugin/AGENTS.md',
  });
  return outputs;
}

// Every file currently under dist/codex/, as POSIX-relative paths, excluding the
// manifest itself (it describes the render; it is not part of the render output).
function actualOutputs(distRoot) {
  const found = new Set();
  function walk(dir, prefix) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const rel = prefix ? `${prefix}/${entry.name}` : entry.name;
      const abs = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        walk(abs, rel);
      } else if (rel !== MANIFEST_NAME) {
        found.add(rel);
      }
    }
  }
  if (fs.existsSync(distRoot)) walk(distRoot, '');
  return found;
}

export function buildManifest(result) {
  const expected = expectedOutputs(result);
  const files = {};
  for (const [rel, { content, source }] of [...expected.entries()].sort()) {
    files[rel] = { source, sha256: sha256(content) };
  }
  return {
    description:
      'Per-output source->dist mapping and content hashes for the Codex render. ' +
      'Regenerated by render-codex.mjs; verified by check-codex-drift.mjs.',
    generator: 'scripts/codex-build/render-codex.mjs',
    counts: { agents: result.agents.length, skills: result.skills.length },
    files,
  };
}

// Compare the fresh render against what is on disk. Returns the list of drift
// entries: missing (expected, absent on disk), extra (on disk, not expected),
// and modified (present but content differs).
export function detectDrift(root) {
  const result = render(root);
  const distRoot = path.join(root, DIST_REL);
  const expected = expectedOutputs(result);
  const actual = actualOutputs(distRoot);

  const drift = [];
  for (const [rel, { content }] of expected.entries()) {
    if (!actual.has(rel)) {
      drift.push({ kind: 'missing', path: rel });
      continue;
    }
    const onDisk = fs.readFileSync(path.join(distRoot, rel), 'utf8');
    if (onDisk !== content) {
      drift.push({ kind: 'modified', path: rel });
    }
  }
  for (const rel of actual) {
    if (!expected.has(rel)) {
      drift.push({ kind: 'extra', path: rel });
    }
  }

  const manifestPath = path.join(distRoot, MANIFEST_NAME);
  const freshManifest = `${JSON.stringify(buildManifest(result), null, 2)}\n`;
  if (!fs.existsSync(manifestPath)) {
    drift.push({ kind: 'missing', path: MANIFEST_NAME });
  } else if (fs.readFileSync(manifestPath, 'utf8') !== freshManifest) {
    drift.push({ kind: 'modified', path: MANIFEST_NAME });
  }

  drift.sort((a, b) => (a.path < b.path ? -1 : a.path > b.path ? 1 : 0));
  return drift;
}

export function formatDrift(drift) {
  const lines = ['dist/codex/ is out of sync with plugin/:'];
  for (const entry of drift) {
    const label =
      entry.kind === 'missing'
        ? 'not generated'
        : entry.kind === 'extra'
          ? 'hand-added / stale (no plugin/ source)'
          : 'differs from the current render';
    lines.push(`  ${entry.kind}: dist/codex/${entry.path} (${label})`);
  }
  lines.push(REMEDIATION);
  return lines.join('\n');
}

function writeManifest(root) {
  const result = render(root);
  const manifest = buildManifest(result);
  const target = path.join(root, DIST_REL, MANIFEST_NAME);
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, `${JSON.stringify(manifest, null, 2)}\n`);
  return target;
}

function parseArgs(argv) {
  const opts = { root: DEFAULT_ROOT, writeManifest: false };
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === '--root') {
      opts.root = path.resolve(argv[++i]);
    } else if (argv[i] === '--write-manifest') {
      opts.writeManifest = true;
    }
  }
  return opts;
}

function main() {
  const opts = parseArgs(process.argv.slice(2));
  if (opts.writeManifest) {
    const target = writeManifest(opts.root);
    process.stdout.write(`wrote ${path.relative(opts.root, target)}\n`);
    return;
  }
  const drift = detectDrift(opts.root);
  if (drift.length > 0) {
    process.stderr.write(`${formatDrift(drift)}\n`);
    process.exitCode = 1;
    return;
  }
  process.stdout.write('dist/codex/ is in sync with plugin/.\n');
}

export { REMEDIATION, MANIFEST_NAME, sha256, expectedOutputs, actualOutputs };

if (isEntryPoint(process.argv[1], import.meta.url)) {
  main();
}
